def array_from_ENDF(fh):
    "Convert a chunk of ENDF, stripped of metadata, into a numpy array."
    return np.genfromtxt(fh,
                         dtype='float',
                         delimiter=11,
                         converters={0: endftod, 1: endftod, 2: endftod,
                                     3: endftod, 4: endftod, 5: endftod})


def test_endftod():
    from pyne._utils import endftod
    obs = [endftod(" 3.28559+12"),
           endftod(" 2.328559+4"),
           endftod(" 3.28559-12"),
           endftod(" 2.328559-2"),
           endftod("-3.28559+12"),
           endftod("-2.328559+2"),
           endftod("-3.28559-12"),
           endftod("-2.328559-2"),
           endftod("        121"),
           endftod("       -121")]
    exp = [ 3.28559e+12,
            2.328559e+4,
            3.28559e-12,
            2.328559e-2,
           -3.28559e+12,
           -2.328559e+2,
           -3.28559e-12,
           -2.328559e-2,
                  121.0,
                 -121.0]
    obs = np.array(obs)
